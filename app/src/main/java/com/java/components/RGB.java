package com.java.components;

public class RGB {
    private int R;

    public int getR() {
        return R;
    }

    private int G;

    public int getG() {
        return G;
    }

    private int B;
    
    public int getB() {
        return B;
    }

    public RGB(int r, int g, int b) {
        R = r;
        G = g;
        B = b;
    }
}
